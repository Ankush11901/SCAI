/**
 * Update the generation history with final HTML
 */
async function finalizeArticle(
  historyId: string,
  html: string,
  wordCount: number,
  successfulImages: number,
  failedImages: number
): Promise<void> {
  // Merge with existing metadata to preserve classificationHint etc.
  const [existing] = await db
    .select({ metadata: generationHistory.metadata })
    .from(generationHistory)
    .where(eq(generationHistory.id, historyId))
    .limit(1);

  let existingMeta: Record<string, unknown> = {};
  try {
    if (existing?.metadata) existingMeta = JSON.parse(existing.metadata);
  } catch { /* ignore */ }

  const metadata = JSON.stringify({
    ...existingMeta,
    imageStats: {
      total: successfulImages + failedImages,
      successful: successfulImages,
      failed: failedImages,
    },
    completedAt: new Date().toISOString(),
    version: "trigger-r2-v1",
  });

  await db.update(generationHistory)
    .set({
      htmlContent: html,
      wordCount,
      status: "completed",
      metadata,
      updatedAt: new Date(),
    })
    .where(eq(generationHistory.id, historyId));
}

/**
 * Escape HTML special characters
 */
function escapeHtml(text: string): string {
  return text
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#039;");
}
