interface PlatformIconProps {
  platform: string;
  className?: string;
  showBackground?: boolean;
}

const platformColors: Record<string, { bg: string; text: string }> = {
  medium: { bg: "bg-[#12100E]", text: "text-white" },
  devto: { bg: "bg-[#0A0A0A]", text: "text-white" },
  ghost: { bg: "bg-[#15171A]", text: "text-white" },
  hashnode: { bg: "bg-[#2962FF]", text: "text-white" },
  shopify: { bg: "bg-[#96BF48]", text: "text-white" },
  webflow: { bg: "bg-[#4353FF]", text: "text-white" },
  hubspot: { bg: "bg-[#FF7A59]", text: "text-white" },
  wix: { bg: "bg-[#0C6EFC]", text: "text-white" },
  blogger: { bg: "bg-[#FF5722]", text: "text-white" },
  contentful: { bg: "bg-[#2478CC]", text: "text-white" },
  strapi: { bg: "bg-[#4945FF]", text: "text-white" },
  drupal: { bg: "bg-[#0678BE]", text: "text-white" },
  bigcommerce: { bg: "bg-[#121118]", text: "text-white" },
};

export function CMSPlatformIcon({
  platform,
  className,
  showBackground = false,
}: PlatformIconProps) {
  const colors = platformColors[platform] || { bg: "bg-neutral-700", text: "text-white" };
  const iconClass = showBackground ? colors.text : "";

  const icons: Record<string, React.ReactNode> = {
    medium: <MediumIcon className={cn(className, iconClass)} />,
    devto: <DevToIcon className={cn(className, iconClass)} />,
    ghost: <GhostIcon className={cn(className, iconClass)} />,
    hashnode: <HashnodeIcon className={cn(className, iconClass)} />,
    shopify: <ShopifyIcon className={cn(className, iconClass)} />,
    webflow: <WebflowIcon className={cn(className, iconClass)} />,
    hubspot: <HubSpotIcon className={cn(className, iconClass)} />,
    wix: <WixIcon className={cn(className, iconClass)} />,
    blogger: <BloggerIcon className={cn(className, iconClass)} />,
    contentful: <ContentfulIcon className={cn(className, iconClass)} />,
    strapi: <StrapiIcon className={cn(className, iconClass)} />,
    drupal: <DrupalIcon className={cn(className, iconClass)} />,
    bigcommerce: <BigCommerceIcon className={cn(className, iconClass)} />,
  };

  const icon = icons[platform] || (
    <span className={cn("text-xs font-bold", className, iconClass)}>
      {platform.charAt(0).toUpperCase()}
    </span>
  );

  if (showBackground) {
    return (
      <div className={cn("p-2 rounded-lg", colors.bg, "flex items-center justify-center")}>
        {icon}
      </div>
    );
  }

  return <>{icon}</>;
}
